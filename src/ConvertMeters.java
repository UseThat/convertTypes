public class ConvertMeters {
    double convertMetersToCenters(double a) {
        return a * 100;
    }

    double convertMetersToMillimeters(double a) {
        return a * 1000;
    }

    double convertCentersToMeters(double a) {
        return a / 100;
    }

    double convertMillimetersToMeters(double a) {
        return a / 1000;
    }
}